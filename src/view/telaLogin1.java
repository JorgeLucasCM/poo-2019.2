    private void botaoEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEntrarActionPerformed
         tipo = (String)comboBox1.getSelectedItem();
       
            if(textoDoUsuario.getText().equals("Digite o Nome") || (textoDoCadeado.getText().equals("Digite a Senha"))){
            JOptionPane.showMessageDialog(null, "Digite o/os Campos!",null,JOptionPane.ERROR_MESSAGE);
            if(textoDoUsuario.getText().equals("Digite o Nome"))
                textoDoUsuario.setBackground(new Color(255,102,102));
            if(textoDoCadeado.getText().equals("Digite a Senha"))
                textoDoCadeado.setBackground(new Color(255,102,102));
            } else if(tipo.equals("Selecione uma opção")){
                comboBox1.setBackground(new Color(255,102,102));
                JOptionPane.showMessageDialog(null, "Selecione uma Opção!",null,JOptionPane.ERROR_MESSAGE);
            
            }else{
   
                if(tipo.equals("Aluno")){
                    if(new AlunoDAO().podeLogar(textoDoUsuario.getText(),textoDoCadeado.getText())){
                        identificacao = new AlunoDAO().alunoComMatricula(Integer.parseInt(textoDoUsuario.getText())).getNome();
                        alunoLogado = new AlunoDAO().alunoComMatricula(Integer.parseInt(textoDoUsuario.getText()));
                        new TelaAluno().setVisible(true);
                        this.dispose();
                    } else{
                        JOptionPane.showMessageDialog(null, "Usuario não encontrado ou Senha inválida!",null,JOptionPane.ERROR_MESSAGE);
                    }                
                } else {
                    if(new BibliotecarioDAO().podeLogar(textoDoUsuario.getText(), textoDoCadeado.getText())){
                        identificacao = textoDoUsuario.getText();
                        new TelaBibliotecario().setVisible(true);
                        this.dispose();
                    }else{
                        JOptionPane.showMessageDialog(null, "Usuario não encontrado ou Senha inválida!",null,JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
            
    }//GEN-LAST:event_botaoEntrarActionPerformed

